def test_video_system():
    system = VideoSystem(program=EXAMPLE_PROGRAM)
    assert system
    assert system.program == EXAMPLE_PROGRAM
    assert system.X == 1
    assert system.clock == 0
    assert system.interesting_cycles == [20, 60, 100, 140, 180, 220]
    assert system.strong_signals == []
    assert system.sprite_pixels == (0, 1, 2)
    assert len(system.CRT) == 6
    assert len(system.CRT[0]) == 40


def test_little_program():
    program = ("noop", "addx 3", "addx -5")
    system = VideoSystem(program)
    system.run_program()
    assert system.X == -1
    assert system.clock == 5

def test_big_program():
    system = VideoSystem(EXAMPLE_PROGRAM)
    system.run_program()
    assert system.strong_signals == [420, 1140, 1800, 2940, 2880, 3960]

def test_drawing():
    system = VideoSystem(EXAMPLE_PROGRAM)
    system.run_program()
    output = [''.join(line) for line in system.CRT]

    assert output == EXAMPLE_CRT
